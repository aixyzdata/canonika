        }
      }
    },
    
    decodeToken(token) {
      try {
        // O Quarter envia um token base64 simples, n√£o JWT
        const payload = JSON.parse(atob(token))
        return payload
      } catch (error) {
        console.error('‚ùå Erro ao decodificar token:', error)
        throw new Error('Token inv√°lido')
      }
    },
    
